let n = 5;              //put the value 5 in variable n
console.log(n);         
n += 5;                 //add 5 in n and put the result into n again
console.log(n); // 10
n -= 5;                 //subtract 5 in n and put the result into n again
console.log(n); // 5

n *= 5;
console.log(n); // 5

n /= 2;
console.log(n); // 5

n %= 2;
console.log(n); // 5

n **= 2;
console.log(n); // 5
